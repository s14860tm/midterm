function plus(a,b){
    var result = a + b;
    return result;
};

function minus(a,b){
    var result = a - b;
    return result;
};

function times(a,b){
    var result = a * b;
    return result;
};

function div(a,b){
    var result = a / b;
    return result;
};

function mod(a,b){
    var result = a % b;
    return result;
};

